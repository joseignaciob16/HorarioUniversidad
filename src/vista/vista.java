    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        if (jComboBox2.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "No ha seleccionado una materia");
        } else {
            if (jTextField14.getText().trim().equals("") || jTextField15.getText().trim().equals("") || jTextField13.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
            } else {

                Double notaFinal = 0d;
                try {
                    notaFinal = ((Double.valueOf(jTextField14.getText()) + Double.valueOf(jTextField15.getText()) + Double.valueOf(jTextField13.getText())) / 3);
                    jTextField12.setText(" " + notaFinal);
                    matricula.getMateriaMatriculada(jComboBox2.getSelectedItem().toString(), Integer.valueOf(jLabel9.getText())).setNotaFinal(notaFinal);

                    jPanel106.setVisible(true);
                    jPanel106.setEnabled(true);
                } catch (Exception io) {
                    JOptionPane.showMessageDialog(null, "Recomendaciones\n1.Escriba las comas como puntos , = Error/ . = OK \n2.Solo puede escribir digitos y coma \".\"");
                }

            }

        }
    }//GEN-LAST:event_jButton20ActionPerformed

