    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped

        if (Character.isLetter(evt.getKeyChar()) || Character.isSpaceChar(evt.getKeyChar()) || Character.isISOControl(evt.getKeyChar())) { //ISO CONTROL ES BORRAR ENTER ETC..
            jPanel2.setVisible(false);
        } else {
            jPanel2.setVisible(true);
            jLabel3.setText(" En este campo solo puede escribir letras");
            evt.consume();
        }


    }//GEN-LAST:event_jTextField1KeyTyped

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        Estudiante estudiante = null;
        try {
            estudiante = matricula.buscarEstudiante(jTextField1.getText().trim());
            if (estudiante == null) {
                jPanel6.setVisible(false);
                jPanel6.setEnabled(false);
                jPanel4.setVisible(true);
                jPanel4.setEnabled(true);
                jLabel4.setText("O.O");
                jLabel5.setText("<html><p style=\"text-align:center\">El nombre " + jTextField1.getText() + " no esta registrado en el sistema.</p></html>");
            } else if (estudiante != null) {
                jPanel4.setVisible(false);
                jPanel4.setEnabled(false);
                jPanel6.setVisible(true);
                jPanel6.setEnabled(true);
                jLabel8.setText(estudiante.getNombre());
                jLabel9.setText(String.valueOf(estudiante.getCodigoEstudiante()));
                jLabel12.setText(estudiante.getCorreo());
            }
        } catch (Exception io) {
            jPanel2.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

